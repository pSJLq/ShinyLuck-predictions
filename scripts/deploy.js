// Deploy PredictionMarket + XOracleResolver to Somnia testnet (or localhost).
//
//   npx hardhat run scripts/deploy.js --network somniaTestnet
//
// Writes deployments/<network>.json and web/config.js.

const { ethers, network } = require("hardhat");
const fs = require("fs");
const path = require("path");

const PLATFORMS = {
  somniaTestnet: "0x037Bb9C718F3f7fe5eCBDB0b600D607b52706776",
  somniaMainnet: "0x5E5205CF39E766118C01636bED000A54D93163E6",
};

// Somnia gotcha (from the casino playbook): let ethers estimate gas itself,
// never hardcode 21000; and never run two owner txs concurrently.
async function main() {
  const [deployer] = await ethers.getSigners();
  const bal = await ethers.provider.getBalance(deployer.address);
  console.log(`deployer=${deployer.address} balance=${ethers.formatEther(bal)} network=${network.name}`);

  let platformAddr = PLATFORMS[network.name];
  if (!platformAddr) {
    // local run: deploy the mock platform so the full stack works offline
    const mock = await (await ethers.getContractFactory("MockAgentPlatform")).deploy();
    await mock.waitForDeployment();
    platformAddr = await mock.getAddress();
    console.log(`mock platform: ${platformAddr}`);
  }

  const pm = await (await ethers.getContractFactory("PredictionMarket")).deploy();
  await pm.waitForDeployment();
  console.log(`PredictionMarket: ${await pm.getAddress()}`);

  const resolver = await (await ethers.getContractFactory("XOracleResolver"))
    .deploy(platformAddr, await pm.getAddress());
  await resolver.waitForDeployment();
  console.log(`XOracleResolver:  ${await resolver.getAddress()}`);

  await (await pm.setResolver(await resolver.getAddress())).wait();
  console.log("market.setResolver wired");

  // Interim x-oracle host (GitHub raw). Swap to shinyluck.win/x-oracle later
  // with a single setOracleBaseUrl tx - live markets keep working.
  const base = process.env.ORACLE_BASE_URL || "";
  if (base) {
    await (await resolver.setOracleBaseUrl(base)).wait();
    console.log(`oracleBaseUrl = ${base}`);
  }

  // Seed the resolver so the first rounds can fire before creation fees accrue.
  const seed = process.env.RESOLVER_SEED_STT || "1";
  if (Number(seed) > 0) {
    await (await deployer.sendTransaction({
      to: await resolver.getAddress(),
      value: ethers.parseEther(seed),
    })).wait();
    console.log(`resolver seeded with ${seed} STT`);
  }

  const manifest = {
    network: network.name,
    chainId: Number((await ethers.provider.getNetwork()).chainId),
    deployedAt: new Date().toISOString(),
    deployer: deployer.address,
    addresses: {
      predictionMarket: await pm.getAddress(),
      xOracleResolver: await resolver.getAddress(),
      agentPlatform: platformAddr,
    },
  };
  const outPath = path.join(__dirname, "..", "deployments", `${network.name}.json`);
  fs.writeFileSync(outPath, JSON.stringify(manifest, null, 2));
  console.log(`manifest -> ${outPath}`);

  const webCfg = `// AUTO-GENERATED by scripts/deploy.js - do not edit by hand.
window.PREDICTIONS_CONFIG = ${JSON.stringify({
    chainId: manifest.chainId,
    rpc: network.name === "somniaTestnet" ? "https://api.infra.testnet.somnia.network" : "http://127.0.0.1:8545",
    predictionMarket: manifest.addresses.predictionMarket,
    xOracleResolver: manifest.addresses.xOracleResolver,
    agentsExplorer: "https://agents.testnet.somnia.network",
  }, null, 2)};
`;
  fs.writeFileSync(path.join(__dirname, "..", "web", "config.js"), webCfg);
  console.log("web/config.js written");
}

main().catch((e) => { console.error(e); process.exit(1); });
